// DATA TYPES
let stringType = "1234";  // string
console.log(typeof stringType);
console.log(Boolean(stringType));  // empty = ""   - is a false value

let numberType = 123145;  // number
console.log(typeof numberType);
console.log(Boolean(numberType));  // zero = 0   - is a false value

let booleanType = true;  // boolean (true, false)
console.log(typeof booleanType);

let nullType = null;  // object (null type)
console.log(typeof nullType);
console.log(Boolean(nullType));  // null is always a false value

let undefinedType;
console.log(typeof undefinedType);  // undefined
console.log(Boolean(undefinedType));  // undefined is always a false value

let nanType = 10 * 'hello';  // number (NaN - not a number)
console.log(typeof nanType, nanType);
console.log(Boolean(nanType));  // NaN is always a false value


